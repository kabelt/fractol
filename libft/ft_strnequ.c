#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (!s1 || !s2)
		return (0);
	while ((*s1 || *s2) && (n > 0))
	{
		if (*s1 == *s2)
		{
			s2++;
			s1++;
			n--;
		}
		else
			return (0);
	}
	return (1);
}
