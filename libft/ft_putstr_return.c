#include "libft.h"

int	ft_putstr_return(char const *s)
{
	int	n;
	int c;

	n = 0;
	c = 0;
	if (!s)
		return (0);
	if (!*s)
	{
		write(1, &c, 1);
		return (1);
	}
	while (*s)
	{
		write(1, &(*(s++)), 1);
		n++;
	}
	return (n);
}
